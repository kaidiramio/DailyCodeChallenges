function makeNegative(num) {
    if (num > 0) {
      return -num
      
  //     if the number is > 0 it'll return the negtive of that number
    } else if (num < 0){
      return num
  //     if the number is less than 0 it'll return the the number (as it's already negative)
    }else{
      return 0
    }
  }